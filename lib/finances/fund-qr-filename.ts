/** Nome de arquivo seguro para PNG do QR de contribuição. */
export function slugifyFundQrFilename(value: string): string {
  return (
    value
      .normalize("NFD")
      .replace(/[\u0300-\u036f]/g, "")
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, "-")
      .replace(/^-|-$/g, "")
      .slice(0, 48) || "fundo"
  );
}
